const PHASE_BY_ROUND = {
  "Round of 32": "r32",
  "Round of 16": "r16",
  "Quarter-final": "qf",
  "Semi-final": "sf",
  "Match for third place": "third_place",
  Final: "final",
};

function escSql(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
}

/** "13:00 UTC-6" + "2026-06-11" -> "2026-06-11T13:00:00-06:00" */
function toIsoTimestamp(date, time) {
  const m = time.match(/^(\d{2}):(\d{2})\s+UTC([+-]\d+)$/);
  if (!m) throw new Error(`Cannot parse time "${time}"`);
  const [, hh, mm, offset] = m;
  const sign = offset[0];
  const hours = String(Math.abs(parseInt(offset, 10))).padStart(2, "0");
  return `${date}T${hh}:${mm}:00${sign}${hours}:00`;
}

const teamsRaw = JSON.parse(fs.readFileSync(TEAMS_PATH, "utf8"));
const matchesRaw = JSON.parse(fs.readFileSync(MATCHES_PATH, "utf8")).matches;

// Map team English name -> FIFA code (so we can resolve match teams)
const NAME_TO_CODE = {};
for (const t of teamsRaw) {
  NAME_TO_CODE[t.name] = t.fifa_code;
  if (t.name_normalised) NAME_TO_CODE[t.name_normalised] = t.fifa_code;
}

// Sort group-stage matches chronologically across the whole stage (FIFA assigns
// match numbers 1..72 in that order)
const groupStage = matchesRaw
  .filter((m) => m.group)
  .map((m) => ({
    ...m,
    isoKickoff: toIsoTimestamp(m.date, m.time),
    groupLetter: m.group.replace("Group ", ""),
  }))
  .sort((a, b) => a.isoKickoff.localeCompare(b.isoKickoff));

// Assign FIFA match number 1..72
groupStage.forEach((m, i) => {
  m.num = i + 1;
});

// For each group, derive md1/md2/md3 by chronological order within the group
const byGroup = {};
for (const m of groupStage) {
  (byGroup[m.groupLetter] ??= []).push(m);
}
for (const g of Object.keys(byGroup).sort()) {
  const sorted = [...byGroup[g]].sort((a, b) =>
    a.isoKickoff.localeCompare(b.isoKickoff),
  );
  sorted.forEach((m, i) => {
    m.matchdayOfGroup = i < 2 ? 1 : i < 4 ? 2 : 3;
  });
}

// Knockout matches keep the `num` field FIFA assigned (73..104). For final/3rd
// place the JSON lacks `num`, so we assign 103 (3rd place) and 104 (final).
const knockout = matchesRaw
  .filter((m) => !m.group)
  .map((m) => ({
    ...m,
    isoKickoff: toIsoTimestamp(m.date, m.time),
  }));

let nextNum = 103;
for (const m of knockout) {
  if (!m.num) {
    m.num = m.round === "Match for third place" ? nextNum++ : nextNum++;
  }
}

// ---------------------------------------------------------------------------
// Build SQL
// ---------------------------------------------------------------------------
const lines = [];
lines.push(
  "-- =============================================================================",
  "-- SEED Mundial 2026 — equipos + 104 partidos",
  "-- Generated by scripts/generate-mundial-seed.mjs from the JSON fixture files.",
  "-- Idempotent: re-running won't create duplicates (uses ON CONFLICT).",
  "-- =============================================================================",
  "",
  "-- TEAMS",
);

for (const t of teamsRaw.sort((a, b) =>
  a.group.localeCompare(b.group) || a.name.localeCompare(b.name),
)) {
  const id = t.fifa_code;
  const name = ES_NAMES[t.name] ?? t.name;
  const flag = t.flag_icon ?? "";
  lines.push(
    `insert into public.teams (id, name, group_letter, flag_emoji) values (${escSql(id)}, ${escSql(name)}, ${escSql(t.group)}, ${escSql(flag)}) on conflict (id) do update set name = excluded.name, group_letter = excluded.group_letter, flag_emoji = excluded.flag_emoji;`,
  );
}

lines.push("", "-- MATCHES (group stage 1..72)");

for (const m of groupStage) {
  const home = NAME_TO_CODE[m.team1];
  const away = NAME_TO_CODE[m.team2];
  if (!home || !away) {
    throw new Error(`No FIFA code for "${m.team1}" or "${m.team2}"`);
  }
  const phase = `group_md${m.matchdayOfGroup}`;
  const venue = m.ground ?? "";
  lines.push(
    `insert into public.matches (id, phase, group_letter, team_home, team_away, kickoff_at, venue) values (${m.num}, '${phase}', ${escSql(m.groupLetter)}, ${escSql(home)}, ${escSql(away)}, ${escSql(m.isoKickoff)}::timestamptz, ${escSql(venue)}) on conflict (id) do update set phase = excluded.phase, group_letter = excluded.group_letter, team_home = excluded.team_home, team_away = excluded.team_away, kickoff_at = excluded.kickoff_at, venue = excluded.venue;`,
  );
}

lines.push("", "-- MATCHES (knockout 73..104)");

for (const m of knockout) {
  const phase = PHASE_BY_ROUND[m.round];
  if (!phase) throw new Error(`Unknown round "${m.round}"`);
  const venue = m.ground ?? "";
  // team1/team2 are placeholders like "2A", "W74", "L101" until the bracket
  // is resolved match-by-match
  lines.push(
    `insert into public.matches (id, phase, team_home_placeholder, team_away_placeholder, kickoff_at, venue) values (${m.num}, '${phase}', ${escSql(m.team1)}, ${escSql(m.team2)}, ${escSql(m.isoKickoff)}::timestamptz, ${escSql(venue)}) on conflict (id) do update set phase = excluded.phase, team_home_placeholder = excluded.team_home_placeholder, team_away_placeholder = excluded.team_away_placeholder, kickoff_at = excluded.kickoff_at, venue = excluded.venue;`,
  );
}

lines.push("");

fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
fs.writeFileSync(OUT_PATH, lines.join("\n"));

console.log(
  `✅ Wrote ${OUT_PATH}\n   Teams: ${teamsRaw.length}\n   Group stage: ${groupStage.length}\n   Knockout: ${knockout.length}\n   Total matches: ${groupStage.length + knockout.length}`,
);
